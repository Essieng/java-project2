public class Codeup1256 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int starCount = scanner.nextInt();
        String star = "*";
        for (int i = 0; i < starCount; i++) {
            System.out.print(star);
        }
    }
}
