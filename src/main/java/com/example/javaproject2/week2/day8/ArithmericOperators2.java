package com.example.javaproject2.week2.day8;

public class ArithmericOperators2 {
    public static void main(String[] args) {
        System.out.println("a = 10, b = 3");
        int a = 10;
        int b = 3;
        System.out.printf("10+b = %d\n",10+b);
        System.out.printf("a-3 = %d\n", a-3);
        System.out.printf("a*b = %d\n", a*b);
        System.out.printf("10*3/b = %d\n", 10*3/b);
        System.out.printf("49%%b = %d\n", 49%b);
    }
}
