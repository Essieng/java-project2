package com.example.javaproject2.week2.day10;

public class ElseExam {
    public static void main(String[] args) {
        boolean checkTrue = true;
        if (checkTrue) {
            System.out.println("true입니다.");
        } else {
            System.out.println("false입니다.");
        }

        checkTrue = false;
        if (checkTrue) {
            System.out.println("true입니다.");
        } else {
            System.out.println("false입니다.");
        }
    }
}
